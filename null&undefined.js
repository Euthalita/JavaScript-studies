let valor //Não inicializada
//Quando a variavel não é inicializada, ou seja, não recebe nenhum valor, ela é undefined(indefinida)
console.log(valor)

valor = null //O null está afirmando que esta variavel não aponta pra nenhum valor na memoria, ela foi inicializa, porem sem valor
console.log(valor)

//O null é usado geralmente para uma variavel de referencia e que você não quer mais q ela aponte para determinado valor na memoria, ou seja, para zerar uma variavel
//Não é possivel acessar valores de uma variavel nula